package trabalhocg.codigos;

import java.io.Serializable;

/**
 *
 * @author vilson
 */
public class Pontos implements Serializable{
    private double x;
    private double y;
    private double z;

    public Pontos() {
        this.x = 0;
        this.y = 0;
        this.z = 0;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getZ() {
        return z;
    }

    public void setZ(double z) {
        this.z = z;
    }
    
}
